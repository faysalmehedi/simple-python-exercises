# This program will convert celsius to Fahrenheit

celsius = float(input("Please enter the celsius: "))

fahrenheit = (celsius * 1.8) + 32

print(f"Temperature {celsius:.2f} degree celsius is equal to {fahrenheit:.2f} degree fahrenheit.")