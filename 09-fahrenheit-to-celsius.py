# This program will convert Fahrenheit to celsius

fahrenheit = float(input("Please enter the fahrenheit: "))

celsius = (fahrenheit - 32) / 1.8

print(f"Temperature {fahrenheit:.2f} degree fahrenheit is equal to {celsius:.2f} degree celsius.")